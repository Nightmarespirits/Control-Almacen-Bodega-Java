    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        Boolean agregarRegistro = contratoDao.agregarContrato(txtNumContrato.getText(), txtCodEmp.getText(), dcFecInicioContrato.getDate(), 
                dcFecFinContrato.getDate(),cboSucursal.getSelectedItem().toString(), cboArea.getSelectedItem().toString(), cboCargo.getSelectedItem().toString(), 
                Integer.parseInt( txtSueldo.getText()), this.cboEstadoContrato.getSelectedItem().toString(),txtareaObservaciones.getText() );
        if( agregarRegistro){
            JOptionPane.showMessageDialog(null,"Usuario  Agregado con exito");
            campos(false);
            botones(true);
            limpiarCampos();
            tblContratos.setModel(contratoDao.crearMdlContrato());
            editMode = false;
        }else{
            JOptionPane.showMessageDialog(null, "Registro No agregado","",0);
            campos(false);
            botones(true);
            limpiarCampos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUsuarioActionPerformed
        // TODO add your handling code here:
        editMode = true;
        String codigo = JOptionPane.showInputDialog(null,"Ingrese Codigo del Empleado");
        Empleado emp = empLog.buscarEmp(codigo);
        if(emp != null){
            if(!empLog.contratoVigenteEmp(codigo)){
                JOptionPane.showMessageDialog(  null , "Registro Encontrado", "Proyecto", 3);
                limpiarCampos();
                cargarVistaNuevoContrato(emp);
                txtNumContrato.setText(contratoDao.generarNumContrato());
                botones(false);
                campos(true);
            }else{
                JOptionPane.showMessageDialog(  null ,"El empleado: " + codigo + "No se puede crear un nuevo contrato  \n El empleado ya tiene un Contrato Vigente", "Proyecto", 0);
            }

        }else{
            JOptionPane.showMessageDialog(  null , "No se ha encontrado en el registro, \n Para registrar un nuevo empleado vaye a Mantenimiento > Empleados", "Proyecto", 0);
        }

    }//GEN-LAST:event_btnAgregarUsuarioActionPerformed

