    void  cargarPane(JPanel frm){
         frm.setSize(Container.getWidth(),Container.getHeight());
         frm.setLocation(0,0);
         this.Container.removeAll();
         this.Container.add(frm,BorderLayout.CENTER);
         this.Container.revalidate();
         this.Container.repaint();
     }
    
